package com.bridgelabz.reflection.intermediate.dynamicmethodinvocation;

public class MathOperation {

    public int add(int a , int b){
        return a + b;

    }

    public int substract(int a , int b){
        return a - b;
        
    }

    public int multiply(int a , int b){
        return a * b;
    }
    
}